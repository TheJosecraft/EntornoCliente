var palabra = prompt("Introduce la palabra a traducir");

switch(palabra){
    case "cat":
        alert("gato");
    break;
    
    case "dog":
        alert("perro");
    break;
        
    case "yellow":
        alert("amarillo");
    break;
        
    case "sleep":
        alert("dormir");
    break;
        
    default:
        alert("Esa palabra no esxiste en este diccionario");
        
}