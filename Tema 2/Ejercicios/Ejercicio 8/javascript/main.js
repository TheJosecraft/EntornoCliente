var marca = prompt("Introduce la marca de un coche");

switch(marca){
    case "BMW":
        alert("Alemana");
    break;
        
    case "Ford":
        alert("Estadounidense");
    break;
        
    case "Peugeot":
        alert("Francesa");
    break;
    
    default:
        alert("No tenemos esa marca");
}